/// Maps the database [KeyType] enum to the [SshKeyAlgorithm] used by the
/// key generation utility.
SshKeyAlgorithm _keyTypeToAlgorithm(KeyType keyType) {
  switch (keyType) {
    case KeyType.rsa2048:
      return SshKeyAlgorithm.rsa2048;
    case KeyType.rsa4096:
      return SshKeyAlgorithm.rsa4096;
    case KeyType.ed25519:
      return SshKeyAlgorithm.ed25519;
  }
}

/// Generates a new SSH key pair in an isolate, encrypts the private key,
/// and persists it to the database.
///
/// Returns the generated [SshKeyEntity] including the public key and
/// fingerprint for display to the user.
final generateKeyProvider =
    FutureProvider.autoDispose.family<SshKeyEntity, GenerateKeyParams>(
  (ref, params) async {
    final repository = ref.read(keyRepositoryProvider);
    final encryptionKey = ref.read(encryptionKeyProvider);

    // Generate the key pair in an isolate to avoid blocking the UI.
    final keyPair = await SshKeyUtils.generateKeyPair(
      algorithm: _keyTypeToAlgorithm(params.keyType),
      comment: params.label,
    );

    // Encrypt the private key before storage. This is a critical security
    // step -- plaintext private keys must never be persisted.
    final encryptedPrivateKey = CryptoUtils.encrypt(
      plaintext: keyPair.privateKeyPem,
      key: encryptionKey,
    );

    final entity = SshKeyEntity(
      id: 0, // Auto-generated by the database.
      label: params.label,
      keyType: params.keyType,
      publicKey: keyPair.publicKeyOpenSsh,
      encryptedPrivateKey: encryptedPrivateKey,
      fingerprint: keyPair.fingerprint,
      createdAt: DateTime.now(),
    );

    final insertedId = await repository.addKey(entity);

    return entity.copyWith(id: insertedId);
  },
);

/// Imports an existing PEM private key from the file system, encrypts it,
/// derives the public key and fingerprint, and stores the result.
///
/// The private key file is read, validated, encrypted, and then stored.
/// The original file on disk is not modified or deleted.
final importKeyProvider =
    FutureProvider.autoDispose.family<SshKeyEntity, ImportKeyParams>(
  (ref, params) async {
    final repository = ref.read(keyRepositoryProvider);
    final encryptionKey = ref.read(encryptionKeyProvider);

    // Read the PEM file from disk.
    final file = File(params.filePath);
    final pemContent = await file.readAsString();

    // Validate that the content looks like a PEM-encoded private key.
    final trimmedPem = pemContent.trim();
    if (!_isValidPemPrivateKey(trimmedPem)) {
      throw const FormatException(
        'The selected file does not contain a valid PEM-encoded private key.',
      );
    }

    // Determine the key type from the PEM header.
    final keyType = _detectKeyTypeFromPem(trimmedPem);

    // Derive public key and fingerprint based on key type.
    String publicKeyOpenSsh;
    String fingerprint;

    if (keyType == KeyType.ed25519) {
      // For Ed25519, the PEM contains seed + public key bytes.
      final keyData = _extractEd25519KeyData(trimmedPem);
      publicKeyOpenSsh = keyData.publicKeyOpenSsh;
      fingerprint = keyData.fingerprint;
    } else {
      // For RSA keys, parse the public components from the PEM directly.
      final rsaPublicKey = _parseRsaPublicKeyFromPem(trimmedPem);
      publicKeyOpenSsh = SshKeyUtils.rsaPublicKeyToOpenSsh(
        rsaPublicKey,
        comment: params.label,
      );
      final publicKeyBlob = _encodeRsaPublicKeyBlob(rsaPublicKey);
      fingerprint = SshKeyUtils.computeFingerprint(publicKeyBlob);
    }

    // Encrypt the private key for secure storage.
    final encryptedPrivateKey = CryptoUtils.encrypt(
      plaintext: trimmedPem,
      key: encryptionKey,
    );

    final entity = SshKeyEntity(
      id: 0, // Auto-generated by the database.
      label: params.label,
      keyType: keyType,
      publicKey: publicKeyOpenSsh,
      encryptedPrivateKey: encryptedPrivateKey,
      fingerprint: fingerprint,
      createdAt: DateTime.now(),
    );

    final insertedId = await repository.addKey(entity);

    return entity.copyWith(id: insertedId);
  },
);

/// Deletes an SSH key by its [id].
final deleteKeyProvider = FutureProvider.autoDispose.family<void, int>(
  (ref, id) async {
    final repository = ref.read(keyRepositoryProvider);
    await repository.deleteKey(id);
  },
);

/// Decrypts a stored private key for use in an SSH connection.
///
/// The returned plaintext PEM should be used immediately and not cached
/// in memory longer than necessary.
final decryptKeyProvider =
    FutureProvider.autoDispose.family<String, String>(
  (ref, encryptedKey) async {
    final repository = ref.read(keyRepositoryProvider);
    return repository.decryptPrivateKey(encryptedKey);
  },
);

