/// PEM header patterns for supported private key formats.
const String _pemRsaHeader = '-----BEGIN RSA PRIVATE KEY-----';
const String _pemOpenSshHeader = '-----BEGIN OPENSSH PRIVATE KEY-----';
const String _pemGenericHeader = '-----BEGIN PRIVATE KEY-----';

/// Validates that a string contains a recognised PEM private key header.
bool _isValidPemPrivateKey(String pem) {
  return pem.startsWith(_pemRsaHeader) ||
      pem.startsWith(_pemOpenSshHeader) ||
      pem.startsWith(_pemGenericHeader);
}

/// Detects the [KeyType] from PEM header content.
///
/// Defaults to [KeyType.rsa4096] for generic PKCS#8 keys since the actual
/// bit length cannot be reliably determined from the header alone.
KeyType _detectKeyTypeFromPem(String pem) {
  if (pem.startsWith(_pemOpenSshHeader)) {
    return KeyType.ed25519;
  }
  if (pem.startsWith(_pemRsaHeader)) {
    // Parse the key to determine bit length.
    try {
      final privateKey = SshKeyUtils.parseRsaPrivateKeyPem(pem);
      final bitLength = privateKey.modulus!.bitLength;
      if (bitLength <= 2048) {
        return KeyType.rsa2048;
      }
      return KeyType.rsa4096;
    } catch (_) {
      return KeyType.rsa4096;
    }
  }
  return KeyType.rsa4096;
}

/// Extracts Ed25519 public key and fingerprint from a PEM-encoded key.
_Ed25519KeyData _extractEd25519KeyData(String pem) {
  // Strip PEM armour and decode.
  final lines = pem
      .split('\n')
      .where((line) =>
          line.isNotEmpty &&
          !line.startsWith('-----BEGIN') &&
          !line.startsWith('-----END'))
      .join();
  final keyBytes = base64.decode(lines);

  // Ed25519 PEM from SshKeyUtils contains: [32-byte seed][32-byte public key]
  const seedLength = 32;
  if (keyBytes.length < seedLength * 2) {
    throw const FormatException(
      'Invalid Ed25519 private key: insufficient key data.',
    );
  }

  final publicKeyBytes = Uint8List.sublistView(
    Uint8List.fromList(keyBytes),
    seedLength,
    seedLength * 2,
  );

  // Build OpenSSH public key blob.
  final blobBuilder = BytesBuilder();
  SshKeyUtils.writeString(blobBuilder, 'ssh-ed25519');
  SshKeyUtils.writeBytes(blobBuilder, publicKeyBytes);
  final publicKeyBlob = Uint8List.fromList(blobBuilder.toBytes());

  final base64Public = base64.encode(publicKeyBlob);
  final publicKeyOpenSsh = 'ssh-ed25519 $base64Public';
  final fingerprint = SshKeyUtils.computeFingerprint(publicKeyBlob);

  return _Ed25519KeyData(
    publicKeyOpenSsh: publicKeyOpenSsh,
    fingerprint: fingerprint,
  );
}

class _Ed25519KeyData {
  const _Ed25519KeyData({
    required this.publicKeyOpenSsh,
    required this.fingerprint,
  });

  final String publicKeyOpenSsh;
  final String fingerprint;
}

/// Parses an RSA private key PEM and extracts the public key components
/// (modulus and public exponent) directly from the PKCS#1 ASN.1 structure.
///
/// PKCS#1 RSAPrivateKey format:
///   RSAPrivateKey ::= SEQUENCE {
///     version           INTEGER,  -- 0
///     modulus           INTEGER,  -- n
///     publicExponent    INTEGER,  -- e
///     privateExponent   INTEGER,  -- d
///     prime1            INTEGER,  -- p
///     prime2            INTEGER,  -- q
///     ...
///   }
RSAPublicKey _parseRsaPublicKeyFromPem(String pem) {
  final lines = pem
      .split('\n')
      .where((line) =>
          line.isNotEmpty &&
          !line.startsWith('-----BEGIN') &&
          !line.startsWith('-----END'))
      .join();
  final keyBytes = base64.decode(lines);
  final asn1Parser = ASN1Parser(keyBytes);
  final topSeq = asn1Parser.nextObject() as ASN1Sequence;

  final modulus = (topSeq.elements[1] as ASN1Integer).valueAsBigInteger;
  final publicExponent = (topSeq.elements[2] as ASN1Integer).valueAsBigInteger;

  return RSAPublicKey(modulus, publicExponent);
}

/// Encodes an RSA public key into the OpenSSH binary blob format.
Uint8List _encodeRsaPublicKeyBlob(RSAPublicKey publicKey) {
  final builder = BytesBuilder();
  SshKeyUtils.writeString(builder, 'ssh-rsa');
  SshKeyUtils.writeMpInt(builder, publicKey.publicExponent!);
  SshKeyUtils.writeMpInt(builder, publicKey.modulus!);
  return Uint8List.fromList(builder.toBytes());
}

