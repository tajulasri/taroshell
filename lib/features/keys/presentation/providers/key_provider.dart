/// Parameters for SSH key generation.
class GenerateKeyParams {
  const GenerateKeyParams({
    required this.label,
    required this.keyType,
  });

  final String label;
  final KeyType keyType;

  @override
  bool operator ==(Object other) =>
      identical(this, other) ||
      other is GenerateKeyParams &&
          runtimeType == other.runtimeType &&
          label == other.label &&
          keyType == other.keyType;

  @override
  int get hashCode => Object.hash(label, keyType);
}

